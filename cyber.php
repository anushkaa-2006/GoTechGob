        <h3>Actual Work For Cyber Security</h3> <!--actual work-->
        <ol>
            <p></p><!--info-->
            <li><b>Risk Assessment and Management:</b></li><!--maintopic-->
            <ul type="disc">
                <li> Cybersecurity professionals identify and evaluate potential risks to an organization's information systems. This involves analyzing vulnerabilities, threats, and the potential impact of security breaches. They prioritize risks and develop strategies to mitigate them, ensuring that resources are allocated effectively to protect critical assets.</li><!--Subtopic-->
            </ul>
            <li><b>Incident Response and Recovery:</b></li>
            <ul type="disc">
                <li>When a security breach occurs, cybersecurity teams respond quickly to contain the incident, minimize damage, and prevent further compromise. They investigate the cause of the breach, eradicate the threat, and restore affected systems. Post-incident, they conduct thorough analyses to learn from the event and improve future responses.
                </li>
                </ul>
                <Li><b>Security Architecture and Engineering: </b></Li>
                <ul type="disc">
                    <li>Designing and implementing secure systems is a fundamental task in cybersecurity. Professionals develop robust security architectures that integrate with an organization's IT infrastructure. This includes selecting and configuring security tools, such as firewalls, intrusion detection systems, and encryption technologies, to protect data and systems from unauthorized access and attacks.
                    </li>
                </ul>
                <Li><b>Threat Intelligence and Analysis:</b></Li>
                <ul type="disc">
                    <li>Cybersecurity experts gather and analyze information about current and emerging threats. They monitor various sources, such as threat intelligence feeds, security blogs, and hacker forums, to stay informed about new attack vectors and vulnerabilities. This knowledge helps them anticipate potential threats and proactively defend against them.     
                    </li>
                </ul>
                <Li><b>Compliance and Governance:</b></Li>
                <ul type="disc">
                    <li>Organizations must adhere to various legal and regulatory requirements related to data protection and cybersecurity. Cybersecurity professionals ensure compliance with these standards, such as GDPR, HIPAA, and PCI DSS. They develop and enforce security policies, conduct regular audits, and provide training to employees to maintain a culture of security awareness.
                    </li>
                </ul>
                <Li><b>Identity and Access Management (IAM):</b></Li>
                <ul type="disc">
                    <li>Controlling who has access to information and systems is a crucial aspect of cybersecurity. IAM involves implementing policies and technologies to manage user identities, authenticate users, and authorize access to resources. This ensures that only legitimate users can access sensitive data and that their activities are monitored for suspicious behavior.
                    </li>
                </ul>
                <Li><b>Security Awareness and Training:</b></Li>
                <ul type="disc">
                    <li> Human error is often a significant factor in security breaches. Cybersecurity teams conduct training sessions and awareness programs to educate employees about best practices for information security. This includes recognizing phishing attempts, creating strong passwords, and understanding the importance of data protection. A well-informed workforce is an essential line of defense against cyber threats.
                    </li>
                </ul>
        </ol>
