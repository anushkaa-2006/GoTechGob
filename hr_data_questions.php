            27:Why do you want to work for our company?
        </div>
        <div class="ans">
            This is essentially the same question as 'Why did you apply for this job?' & 'Why do you want to work
            here?'. Reply to it by showing that you have researched the employer and have logically thought out reasons
            why you think you are a good match for their requirements. Turn the ANSWER: into a compliment to the company
            you are interviewing for.
            ANSWER:s:
            There are only a limited number of opportunities for advancement with my current employer. Which is why I'm
            keen on working for a larger corporation like yours where I believe there are more possibilities to show
            what I'm capable of.
            A common way to reply to this is to research the company before hand, find
            something unique about them and then give that as your ANSWER:. For instance they may have opened up a new
            store or launched a new product.
            Give examples of positive things you have heard about them and say you want to be associated with a
            reputable brand.
            You believe their work environment is more fun, energetic and rewarding.
            I believe your company will help me to develop my career in the direction I want it to go.

