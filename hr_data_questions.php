        <div class="ans">
            This is the same as asking why have you changed jobs so frequently? It is likely to be asked if your
            curriculum vitae shows that not held down a long term position but have regularly jumped from one job to
            another. When answering you have no real option but to lump all your previous employers together. At all
            costs avoid showing that you are a difficult personality to get along with or that you have a habit of
            leaving jobs abruptly. ANSWER:s:
            I do not believe that my career history is an accurate reflection of my character or abilities. Stability is
            important to me and I am anxious to work long term for one company. Although my CV shows that I have worked
            for a five different companies over a short period of time, the reason for this was that I had a elderly and
            frail relative to look after during this period.
            Say that previous job roles failed to challenge you.
            State that you current desire is to find long term employment and then give a reason for this i.e. your
            recently bought a house, just got married or are in a new committed long term relationship etc.
            Mention and highlight any jobs you have held for long periods.
            Point out the duties in the job you are applying for and state that these are the challenges you have been
            looking for.

