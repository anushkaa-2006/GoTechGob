            31:Where do you see yourself in say four years' time?
        </div>
        <div class="ans">
            This is also the same question as 'Where do you want to be in 4 years?' The recruiter does not want to know
            about your personal goals i.e. you want a house in the south of France etc. They are only interested in your
            career ambitions. Try not to sound too ambitious, for instance saying you want to have been promoted to the
            head of your department. This can scare off some interviewers who may think your after their job! Instead
            sound ambitious but realistic.
            ANSWER:
            In four years time I aim to have improved my abilities and to be a leader in my field. This will mean that I
            can contribute more to my employers and their business.That really depends on how well I perform in my job
            and also what career opportunities come my way. However the bottom line is that I want to have improved my
            skill sets and be making a ongoing contribution to any organization that i happen to be working for.

